#suma y resta
suma = 12 + 5
resta = 12 - 5

#multi y div
multi = 12 * 5
divi = 12 / 5 #nos devuelte un dato flotante

#potentacion (exponente)
exponente = 12 ** 5

#division baja (//)
division_baja = 12 // 5 #devuelve entero redondeado hacia abajo

#resto o modulo 
resto = 12 % 5

#ver el tipo de dato
tipo_de_dato = type(divi)

print(suma)
print(resta)
print(multi)
print(divi)
print(tipo_de_dato)
print(exponente)
print(division_baja)
print(resto)

